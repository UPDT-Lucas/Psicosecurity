    private void refreshImageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_refreshImageMouseClicked
    try {
        if(!selectedEmp.getFirst_name().equals(frstNameField.getText())){
            if(extra_methods.isAlpha(content, frstNameField.getText())){
                if(extra_methods.confirmChanges(content, confirmChanges)){
                    String newFrstName = frstNameField.getText();
                    upd_procs.update_employee_first_name(newFrstName, selectedEmp.getId_employee());
                    confirmChanges = true;
                }  
            }

        }
        if(!selectedEmp.getSecond_name().equals(scndNameField.getText())){  
            if(extra_methods.isAlpha(content, scndNameField.getText())){
                if(extra_methods.confirmChanges(content, confirmChanges)){
                    String newScndName = scndNameField.getText(); 
                    upd_procs.update_employee_second_name(newScndName, selectedEmp.getId_employee());
                    confirmChanges = true;
                }
            }
        }
        if(!selectedEmp.getFirst_surname().equals(frstSurnameField.getText())){    
            if(extra_methods.isAlpha(content, frstSurnameField.getText())){
                if(extra_methods.confirmChanges(content, confirmChanges)){
                    String newFrstSurname = frstSurnameField.getText();  
                    upd_procs.update_employee_first_surname(newFrstSurname, selectedEmp.getId_employee());
                    confirmChanges = true;
                }
            }
        }
        if(!selectedEmp.getSecond_surname().equals(scndSurnameField.getText())){
            if(extra_methods.isAlpha(content, scndSurnameField.getText())){
                if(extra_methods.confirmChanges(content, confirmChanges)){
                    String newScndSurname = scndSurnameField.getText();
                    upd_procs.update_employee_second_surname(newScndSurname, selectedEmp.getId_employee());
                    confirmChanges = true;
                }
            }
        }
        if(extra_methods.idCardCheck(content, idCardField.getText())){
            if(selectedEmp.getId_card() != Integer.parseInt(idCardField.getText())){
                if(extra_methods.confirmChanges(content, confirmChanges)){
                    int newIdCard = Integer.parseInt(idCardField.getText());
                    upd_procs.update_employee_id_card(newIdCard, selectedEmp.getId_employee());
                    confirmChanges = true;
                }
            }
        }
        if(!get_procs.get_civil_status(selectedEmp.getId_civil_status()).getName_status().equals(civilStatusCombo.getSelectedItem())){
            if(extra_methods.confirmChanges(content, confirmChanges)){
                String nameCivilStatus = (String) civilStatusCombo.getSelectedItem();
                civil_status selectedStatus = get_procs.get_civil_status_by_name(nameCivilStatus);
                upd_procs.update_employee_civil_status(selectedStatus.getId_civil_status(), selectedEmp.getId_employee());
                confirmChanges = true;
            }
        }
        if(extra_methods.isNumeric(content, childrenCountField.getText())){
            if(selectedEmp.getChildren_count() != Integer.parseInt(childrenCountField.getText())){
                if(extra_methods.confirmChanges(content, confirmChanges)){
                    int newChildrenCount = Integer.parseInt(childrenCountField.getText());
                    upd_procs.update_employee_children_count(newChildrenCount, selectedEmp.getId_employee());
                    confirmChanges = true;
                }
            }
        }
        if(!selectedEmp.getBirth_date().equals(birthDateChooser.getDate())){
            if(extra_methods.confirmChanges(content, confirmChanges)){
                long d = birthDateChooser.getDate().getTime();
                java.sql.Date newDate = new java.sql.Date(d);
                upd_procs.update_employee_birth_date(newDate, selectedEmp.getId_employee());
                confirmChanges = true;
            }
        }
        extra_methods.changesAdvice(content, confirmChanges);
        updateFields();
        confirmChanges = false;
    
    } catch (ClassNotFoundException ex) {
        Logger.getLogger(personalInfo.class.getName()).log(Level.SEVERE, null, ex);
    } catch (SQLException ex) {
                Logger.getLogger(personalInfo.class.getName()).log(Level.SEVERE, null, ex);
    }  
    }//GEN-LAST:event_refreshImageMouseClicked

