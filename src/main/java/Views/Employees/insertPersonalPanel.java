    private void nextImageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_nextImageMouseClicked
        try {
            if(!(cedField.getText().equals("") || frstNameField.getText().equals("") ||
                    scndNameField.getText().equals("") || frstSurField.getText().equals("") ||
                    scndSurField.getText().equals("") || childrenField.getText().equals("") ||
                    birthDateChooser.getDate() == null))
            {
                if(extra_methods.idCardCheck(content,cedField.getText()) && extra_methods.isAlpha(content,frstNameField.getText()) && 
                       extra_methods.isAlpha(content,scndNameField.getText()) && extra_methods.isAlpha(content,frstSurField.getText()) && 
                       extra_methods.isAlpha(content,scndSurField.getText()) && extra_methods.isNumeric(content,childrenField.getText())){
                    System.out.println( birthDateChooser.getDate().getTime());
                    int ced = Integer.parseInt(cedField.getText());
                    String firstName = frstNameField.getText();
                    String secondName = scndNameField.getText();
                    String firstSurname = frstSurField.getText();
                    String secondSurname = scndSurField.getText();
                    long date = birthDateChooser.getDate().getTime();
                    java.sql.Date birthDate = new java.sql.Date(date);
                    String civilStatusName  = (String) civilStatusCombo.getSelectedItem();
                    int idCivilStatus = get_procs.get_civil_status_by_name(civilStatusName).getId_civil_status();
                    int childrenCount = Integer.parseInt(childrenField.getText());

                    ArrayList<Object> personalInfoList = new ArrayList();
                    personalInfoList.add(firstName);
                    personalInfoList.add(secondName);
                    personalInfoList.add(firstSurname);
                    personalInfoList.add(secondSurname);
                    personalInfoList.add(ced);
                    personalInfoList.add(birthDate);
                    personalInfoList.add(idCivilStatus);
                    personalInfoList.add(childrenCount);

                    insertCompanyPanel p = new insertCompanyPanel(personalInfoList);
                    extra_methods.showPanel(p, content);
                }
            }else{
                JOptionPane.showMessageDialog(content, "Todos los datos de este panel son obligatorios", "ERROR: Faltan campos de llenar", JOptionPane.ERROR_MESSAGE);
            }

            
        } catch (SQLException ex) {
            Logger.getLogger(insertPersonalPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(insertPersonalPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_nextImageMouseClicked

