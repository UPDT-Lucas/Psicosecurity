    private void nextImageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_nextImageMouseClicked
    try {
        if(entryDateChooser.getDate() != null){
            String post = (String) postCombo.getSelectedItem();
            int idPost = get_procs.get_post_by_name(post).getId_post();
            String campus = (String) campusCombo.getSelectedItem();
            int idCampus = get_procs.get_campus_by_name(campus).getId_campus();
            String status = (String) statusCombo.getSelectedItem();
            int idStatus = get_procs.get_status_by_name(status).getId_status();

            Date date = entryDateChooser.getDate();
            long d = date.getTime();
            entryDate = new java.sql.Date(d);

            ArrayList<Object> companyInfoList = new ArrayList();
            companyInfoList.add(idPost);
            companyInfoList.add(idCampus);
            companyInfoList.add(idStatus);
            companyInfoList.add(entryDate);

            insertContactPanel p = new insertContactPanel(infoList, companyInfoList);
            extra_methods.showPanel(p, content);
        }else{
            JOptionPane.showMessageDialog(content, "Todos los datos de este panel son obligatorios", "ERROR: Faltan campos de llenar", JOptionPane.ERROR_MESSAGE);
        }
       
                
    } catch (SQLException ex) {
        Logger.getLogger(insertCompanyPanel.class.getName()).log(Level.SEVERE, null, ex);
    } catch (ClassNotFoundException ex) {
        Logger.getLogger(insertCompanyPanel.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_nextImageMouseClicked

